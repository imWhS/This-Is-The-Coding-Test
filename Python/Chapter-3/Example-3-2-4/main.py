#1이 될 때까지

N, K = map(int, input().split())
cnt = int()

#N이 K보다 작으면 절대 K로 나누어 떨어질 수 없음을 의미하기에 while을 실행할 필요 없다.
while N >= K:
    # N이 K보다 크다는 것은, N에서 일정 값을 뺐을 때 K로 나누어 떨어질 수 있음을 의미한다.
    cnt += N % K
    N -= N % K

    #N이 나누어 떨어지는 수가 되면, 나눈 후의 몫을 구한다. 이후에도 K보다 크면 언제든지 N으로 나누어 떨어질 수 있음을 의미하기에 다시 while을 반복한.
    cnt += 1
    N = N // K

#N이 K보다 작으면 더 이상 2번 과정인 K로 나눈 후의 몫을 도출할 수 없어 1이 될 때까지 빼기만 진행할 수밖에 없다.
while N > 1:
    cnt += 1
    N -= 1

print(cnt)
